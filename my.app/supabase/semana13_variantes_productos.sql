create or replace function public.crear_orden_completa(
  p_usuario_id uuid,
  p_items jsonb,
  p_total numeric
)
returns table (
  orden_id bigint,
  success boolean,
  error_msg text
) as $$
declare
  v_orden_id bigint;
  v_item jsonb;
  v_producto record;
  v_variante record;
  v_producto_id integer;
  v_variante_id bigint;
  v_cantidad integer;
  v_precio numeric;
  v_total_calculado numeric := 0;
begin
  if p_usuario_id is null then
    raise exception 'Usuario inválido';
  end if;

  if p_items is null or jsonb_array_length(p_items) = 0 then
    raise exception 'El carrito está vacío';
  end if;

  for v_item in select * from jsonb_array_elements(p_items)
  loop
    v_producto_id := (v_item ->> 'producto_id')::integer;
    v_variante_id := nullif(v_item ->> 'producto_variante_id', '')::bigint;
    v_cantidad := (v_item ->> 'cantidad')::integer;

    if v_producto_id is null or v_cantidad is null or v_cantidad <= 0 then
      raise exception 'Ítem inválido';
    end if;

    if v_variante_id is not null then
      select
        pv.id,
        pv.producto_id,
        pv.nombre,
        pv.color,
        pv.tamanio,
        pv.precio,
        pv.stock,
        pv.imagen_url,
        p.nombre as producto_nombre
      into v_variante
      from public.producto_variantes pv
      join public.productos p on p.id = pv.producto_id
      where pv.id = v_variante_id
        and pv.producto_id = v_producto_id
      for update of pv;

      if not found then
        raise exception 'Variante no encontrada';
      end if;

      if v_variante.stock < v_cantidad then
        raise exception 'Stock insuficiente para %', v_variante.nombre;
      end if;

      v_total_calculado := v_total_calculado + (v_variante.precio * v_cantidad);
    else
      select id, nombre, precio, stock
      into v_producto
      from public.productos
      where id = v_producto_id
      for update;

      if not found then
        raise exception 'Producto no encontrado';
      end if;

      if v_producto.stock < v_cantidad then
        raise exception 'Stock insuficiente para %', v_producto.nombre;
      end if;

      v_total_calculado := v_total_calculado + (v_producto.precio * v_cantidad);
    end if;
  end loop;

  if v_total_calculado <> p_total then
    raise exception 'Total inválido';
  end if;

  insert into public.ordenes (usuario_id, total, estado)
  values (p_usuario_id, v_total_calculado, 'pendiente')
  returning id into v_orden_id;

  for v_item in select * from jsonb_array_elements(p_items)
  loop
    v_producto_id := (v_item ->> 'producto_id')::integer;
    v_variante_id := nullif(v_item ->> 'producto_variante_id', '')::bigint;
    v_cantidad := (v_item ->> 'cantidad')::integer;

    if v_variante_id is not null then
      select
        pv.id,
        pv.producto_id,
        pv.nombre,
        pv.color,
        pv.tamanio,
        pv.precio,
        pv.stock,
        pv.imagen_url,
        p.nombre as producto_nombre
      into v_variante
      from public.producto_variantes pv
      join public.productos p on p.id = pv.producto_id
      where pv.id = v_variante_id
        and pv.producto_id = v_producto_id
      for update of pv;

      insert into public.order_items (
        orden_id,
        producto_id,
        producto_variante_id,
        nombre,
        variante_nombre,
        color,
        tamanio,
        precio,
        cantidad,
        subtotal,
        imagen_url
      )
      values (
        v_orden_id,
        v_variante.producto_id,
        v_variante.id,
        v_variante.producto_nombre,
        v_variante.nombre,
        v_variante.color,
        v_variante.tamanio,
        v_variante.precio,
        v_cantidad,
        v_variante.precio * v_cantidad,
        v_variante.imagen_url
      );

      update public.producto_variantes
      set stock = stock - v_cantidad
      where id = v_variante.id;
    else
      select id, nombre, precio, stock
      into v_producto
      from public.productos
      where id = v_producto_id
      for update;

      insert into public.order_items (
        orden_id,
        producto_id,
        nombre,
        precio,
        cantidad,
        subtotal
      )
      values (
        v_orden_id,
        v_producto.id,
        v_producto.nombre,
        v_producto.precio,
        v_cantidad,
        v_producto.precio * v_cantidad
      );

      update public.productos
      set stock = stock - v_cantidad
      where id = v_producto.id;
    end if;
  end loop;

  delete from public.carrito
  where usuario_id = p_usuario_id;

  return query select v_orden_id, true, null::text;
exception when others then
  return query select null::bigint, false, sqlerrm;
end;
$$ language plpgsql security definer set search_path = public;

grant execute on function public.crear_orden_completa(uuid, jsonb, numeric)
to authenticated, service_role;
