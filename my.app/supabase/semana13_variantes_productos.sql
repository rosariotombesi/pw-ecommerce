create table if not exists public.producto_variantes (
  id bigint generated by default as identity primary key,
  producto_id integer not null references public.productos(id) on delete cascade,
  nombre varchar(120) not null,
  color varchar(60),
  tamanio varchar(60),
  precio numeric not null check (precio >= 0),
  stock integer not null default 0 check (stock >= 0),
  imagen_url varchar(255),
  alt varchar(255),
  estado varchar(50) not null default 'Disponible',
  creado_en timestamptz not null default now()
);

create index if not exists producto_variantes_producto_id_idx
on public.producto_variantes(producto_id);

create unique index if not exists producto_variantes_unicas_idx
on public.producto_variantes(
  producto_id,
  coalesce(color, ''),
  coalesce(tamanio, '')
);

alter table public.producto_variantes enable row level security;

drop policy if exists "Todos pueden ver variantes" on public.producto_variantes;
drop policy if exists "Admins actualizan variantes" on public.producto_variantes;
drop policy if exists "Admins insertan variantes" on public.producto_variantes;
drop policy if exists "Admins eliminan variantes" on public.producto_variantes;

create policy "Todos pueden ver variantes"
on public.producto_variantes
for select
to public
using (true);

create policy "Admins actualizan variantes"
on public.producto_variantes
for update
to public
using (public.usuario_es_admin(auth.uid()))
with check (public.usuario_es_admin(auth.uid()));

create policy "Admins insertan variantes"
on public.producto_variantes
for insert
to public
with check (public.usuario_es_admin(auth.uid()));

create policy "Admins eliminan variantes"
on public.producto_variantes
for delete
to public
using (public.usuario_es_admin(auth.uid()));

alter table public.carrito
add column if not exists producto_variante_id bigint references public.producto_variantes(id);

alter table public.order_items
add column if not exists producto_variante_id bigint references public.producto_variantes(id),
add column if not exists variante_nombre varchar(120),
add column if not exists color varchar(60),
add column if not exists tamanio varchar(60),
add column if not exists imagen_url varchar(255);

alter table public.carrito
drop constraint if exists carrito_usuario_id_producto_id_key;

drop index if exists public.carrito_usuario_id_producto_id_key;
drop index if exists public.carrito_usuario_producto_base_unique;
drop index if exists public.carrito_usuario_producto_variante_unique;

create unique index carrito_usuario_producto_base_unique
on public.carrito(usuario_id, producto_id)
where producto_variante_id is null;

create unique index carrito_usuario_producto_variante_unique
on public.carrito(usuario_id, producto_variante_id)
where producto_variante_id is not null;

